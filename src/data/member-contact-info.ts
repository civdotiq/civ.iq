/**
 * Get contact information for a member by bioguide ID
 */
export function getMemberContactInfo(bioguideId: string): MemberContactInfo | undefined {
  return MEMBER_CONTACT_INFO[bioguideId];
}

/**
 * Get official website URL for a member
 */
export function getMemberWebsiteUrl(bioguideId: string): string | undefined {
  return MEMBER_CONTACT_INFO[bioguideId]?.url;
}

/**
 * Get contact form URL for a member (explicit or derived from website)
 */
export function getMemberContactFormUrl(bioguideId: string): string | undefined {
  const info = MEMBER_CONTACT_INFO[bioguideId];
  if (!info) return undefined;

  // Return explicit contact form if available
  if (info.contactForm) return info.contactForm;

  // Otherwise derive from official website (most use /contact path)
  const baseUrl = info.url.endsWith('/') ? info.url.slice(0, -1) : info.url;
  return `${baseUrl}/contact`;
}
